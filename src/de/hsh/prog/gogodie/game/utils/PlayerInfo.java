package de.hsh.prog.gogodie.game.utils;

public class PlayerInfo {
	
	private String name;
	private long score;
	
	public PlayerInfo() {}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getScore() {
		return score;
	}
	public void setScore(long score) {
		this.score = score;
	}
	
}
